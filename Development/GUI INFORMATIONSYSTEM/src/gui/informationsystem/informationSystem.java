     private String stringGenerator(String value){
        String data = "";
        String [] dipin = value.split(",");
        if(dipin.length == 0 || dipin.length == 1){
            return value;
        }
        else if(dipin.length > 1){
            for(int i = 0; i < dipin.length; i++){
                if(i < dipin.length - 1){
                    data = data + dipin[i] + ", ";
                }else{
                    data = data + " and " + dipin[i];
                }
            }
        }
        return data;
    }    
    private int binarySearch(ArrayList<GUIINFORMATIONSYSTEM> a, int low, int high,int value){
        if(low <= high){
            int mid = (low + high) / 2;
            if(a.get(mid).getPrice() == value){
                return mid;
            }else if(a.get(mid).getPrice() > value){
                return binarySearch(a, low, mid - 1, value);
            }else{
                return binarySearch(a, mid + 1, high, value);
            }
        }else{
            return -1;
        }
}

    private static void merge(ArrayList<GUIINFORMATIONSYSTEM> first,ArrayList<GUIINFORMATIONSYSTEM>second, ArrayList<GUIINFORMATIONSYSTEM> sPrice){
        int iFirst =0;
        int iSecond= 0;
        int j = 0;
        
        while(iFirst<first.size() && iSecond<second.size()){
            if(first.get(iFirst).getPrice()<second.get(iSecond).getPrice()){
                sPrice.set(j,first.get(iFirst));
                iFirst++;
            }else{
                sPrice.set(j,second.get(iSecond));
                iSecond++;
            }
            j++;
        }
        while(iFirst<first.size()){
            sPrice.set(j,first.get(iFirst));
            iFirst++; j++;
        }
        while(iSecond < second.size()){
             sPrice.set(j,second.get(iSecond));
           
            iSecond++; j++;
            
        }
    }
    public static void sort(ArrayList<GUIINFORMATIONSYSTEM> bPrice) {
        
        if (bPrice.size()<=1)
        {
            return;
        }
        int  a = bPrice.size()/2;
        int b = bPrice.size()-a;
        ArrayList<GUIINFORMATIONSYSTEM> first = new ArrayList ();
        ArrayList<GUIINFORMATIONSYSTEM> second = new ArrayList ();
        
        for(int i=0; i<a; i++){
            first.add(i,bPrice.get(i));
        }for (int i=0;i<b;i++){
            second.add(i,bPrice.get(first.size()+i)); 
        }
        sort(first);
        sort(second);
        merge(first,second,bPrice);
    }
    
