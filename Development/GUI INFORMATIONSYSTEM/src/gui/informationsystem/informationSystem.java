    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try{
            if(jTextField1.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this,"Enter Amount of Price.","Error",JOptionPane.ERROR_MESSAGE);
            }
            else if(list.isEmpty()){
                JOptionPane.showMessageDialog(this,"Add data to search.","Error",JOptionPane.ERROR_MESSAGE);       
            }
            else{
               int neer = Integer.parseInt(jTextField1.getText().trim());
                for(int i = 0; i < list.size(); i++){
                    int minPos = list.indexOf(list.get(i));
                    for(int j = i + 1; j < list.size(); j++){
                        if(list.get(j).getPrice() < list.get(minPos).getPrice()){
                            minPos = list.indexOf(list.get(j));
                        }
                    }
                    
                    GUIINFORMATIONSYSTEM temp = list.get(i);
                    list.set(i,list.get(minPos));
                    list.set(minPos, temp);
                    
                }
               
                int index = binarySearch(list, 0,list.size() - 1, neer);
                if(index == -1){
                    JOptionPane.showMessageDialog(this,"Sorry,there is no hostel for this price.","Error",JOptionPane.ERROR_MESSAGE);
                   jTextField1.setText("");
                }
                else{
                    JOptionPane.showMessageDialog(this,"There is "+ list.get(index).getHostel()+" Hostel for this price.","Data",1);
                    jTextField1.setText("");
                }
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Enter number.","Invalid Input",JOptionPane.ERROR_MESSAGE);
        }
                                         
    
    }//GEN-LAST:event_jButton2ActionPerformed

